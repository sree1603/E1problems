public class ss{
    public static void main(String args[]){
        switch ('+'){
            case '+': 
            System.out.println("s");
            case '=':
                System.out.println("y");
            default:
                System.out.println("c");
            case '/':
                System.out.println("r");
            case '$':
                System.out.println("v");
        }
    }
}